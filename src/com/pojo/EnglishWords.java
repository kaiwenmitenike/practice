package com.pojo;

public class EnglishWords {
	private String id;
	private String englishWords;
	private String englishMean;
	private String addTime;
	private String englishPhrases;
	private String phrasesMean;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEnglishWords() {
		return englishWords;
	}
	public void setEnglishWords(String englishWords) {
		this.englishWords = englishWords;
	}
	public String getEnglishMean() {
		return englishMean;
	}
	public void setEnglishMean(String englishMean) {
		this.englishMean = englishMean;
	}
	public String getAddTime() {
		return addTime;
	}
	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}
	public String getEnglishPhrases() {
		return englishPhrases;
	}
	public void setEnglishPhrases(String englishPhrases) {
		this.englishPhrases = englishPhrases;
	}
	public String getPhrasesMean() {
		return phrasesMean;
	}
	public void setPhrasesMean(String phrasesMean) {
		this.phrasesMean = phrasesMean;
	}
	
}
